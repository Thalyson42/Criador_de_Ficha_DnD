package jdbc.model.characterSheet.attribute;

public class SavingThrows {
	private int idSavingThrows;
	private int savingStrength = 0;
	private int savingDexterity = 0;
	private int savingContitution = 0;
	private int savingIntelligence = 0;
	private int savingWisdom = 0;
	private int savingCharisma = 0;
	
	public int getIdSavingThrows() {
		return idSavingThrows;
	}
	public void setIdSavingThrows(int idSavingThrows) {
		this.idSavingThrows = idSavingThrows;
	}
	public int getSavingStrength() {
		return savingStrength;
	}
	public void setSavingStrength(int savingStrength) {
		this.savingStrength = savingStrength;
	}
	public int getSavingDexterity() {
		return savingDexterity;
	}
	public void setSavingDexterity(int savingDexterity) {
		this.savingDexterity = savingDexterity;
	}
	public int getSavingContitution() {
		return savingContitution;
	}
	public void setSavingContitution(int savingContitution) {
		this.savingContitution = savingContitution;
	}
	public int getSavingIntelligence() {
		return savingIntelligence;
	}
	public void setSavingIntelligence(int savingIntelligence) {
		this.savingIntelligence = savingIntelligence;
	}
	public int getSavingWisdom() {
		return savingWisdom;
	}
	public void setSavingWisdom(int savingWisdom) {
		this.savingWisdom = savingWisdom;
	}
	public int getSavingCharisma() {
		return savingCharisma;
	}
	public void setSavingCharisma(int savingCharisma) {
		this.savingCharisma = savingCharisma;
	}
	
}
