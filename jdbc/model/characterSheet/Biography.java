package jdbc.model.characterSheet;

public class Biography {
    private int idBio;
    private String characterName;
    private String age;  
    private String height;
    private String weight;
    private String eyes;
    private String skin;
    private String hair;
    private String characterAppearance;
    private String alliesOrganizations;
    private String characterBackstory;
    private String additionalFeaturesTraits;
    private String treasure;
    
	public int getIdBio() {
		return idBio;
	}
	
	public void setIdBio(int idBio) {
		this.idBio = idBio;
	}
	
	public String getCharacterName() {
		return characterName;
	}
	
	public void setCharacterName(String characterName) {
		this.characterName = characterName;
	}
	
	public String getAge() {
		return age;
	}
	
	public void setAge(String age) {
		this.age = age;
	}
	
	public String getHeight() {
		return height;
	}
	
	public void setHeight(String height) {
		this.height = height;
	}
	
	public String getWeight() {
		return weight;
	}
	
	public void setWeight(String weight) {
		this.weight = weight;
	}
	
	public String getEyes() {
		return eyes;
	}
	
	public void setEyes(String eyes) {
		this.eyes = eyes;
	}
	
	public String getSkin() {
		return skin;
	}
	
	public void setSkin(String skin) {
		this.skin = skin;
	}
	
	public String getHair() {
		return hair;
	}
	
	public void setHair(String hair) {
		this.hair = hair;
	}
	
	public String getCharacterAppearance() {
		return characterAppearance;
	}
	
	public void setCharacterAppearance(String characterAppearance) {
		this.characterAppearance = characterAppearance;
	}
	
	public String getAlliesOrganizations() {
		return alliesOrganizations;
	}
	
	public void setAlliesOrganizations(String alliesOrganizations) {
		this.alliesOrganizations = alliesOrganizations;
	}
	
	public String getCharacterBackstory() {
		return characterBackstory;
	}
	
	public void setCharacterBackstory(String characterBackstory) {
		this.characterBackstory = characterBackstory;
	}
	
	public String getAdditionalFeaturesTraits() {
		return additionalFeaturesTraits;
	}
	
	public void setAdditionalFeaturesTraits(String additionalFeaturesTraits) {
		this.additionalFeaturesTraits = additionalFeaturesTraits;
	}
	
	public String getTreasure() {
		return treasure;
	}
	
	public void setTreasure(String treasure) {
		this.treasure = treasure;
	}
      
}
